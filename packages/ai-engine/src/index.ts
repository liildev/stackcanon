export function renderAgentsDocument(input: AgentsDocumentInput): string {
  const lines = [
    `<!-- ${STACKCN_MANAGED_MARKER} -->`,
    "# AGENTS.md",
    "",
    "## Core Instruction",
    "Use the generated ai/ directory as the single source of truth for project-specific AI guidance.",
    "",
    "## Current Setup",
    `- Framework: ${input.framework}`,
    `- Quality provider: ${input.quality}`
  ];

  appendPackDetails(lines, input.pack);

  lines.push(
    "",
    "## Read Before Coding",
    ...input.contextFiles.map((contextFile) => `- ${contextFile}`),
    "",
    "Only read the sections relevant to the current task before making changes."
  );

  return `${lines.join("\n")}\n`;
}

